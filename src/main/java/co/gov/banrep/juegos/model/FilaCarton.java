package co.gov.banrep.juegos.model;

public class FilaCarton {
	private Integer ColB;
	private Integer ColI;
	private Integer ColN;
	private Integer ColG;
	private Integer ColO;
	public FilaCarton(Integer colB, Integer colI, Integer colN, Integer colG, Integer colO) {
		super();
		ColB = colB;
		ColI = colI;
		ColN = colN;
		ColG = colG;
		ColO = colO;
	}
	public Integer getColB() {
		return ColB;
	}
	public void setColB(Integer colB) {
		ColB = colB;
	}
	public Integer getColI() {
		return ColI;
	}
	public void setColI(Integer colI) {
		ColI = colI;
	}
	public Integer getColN() {
		return ColN;
	}
	public void setColN(Integer colN) {
		ColN = colN;
	}
	public Integer getColG() {
		return ColG;
	}
	public void setColG(Integer colG) {
		ColG = colG;
	}
	public Integer getColO() {
		return ColO;
	}
	public void setColO(Integer colO) {
		ColO = colO;
	}
	

}
